#include <iostream>
using namespace std;

int main() {
    int num1;
    int num2;
    cout << "Enter First number : ";
    cin >> num1;
    cout << "Enter Second number : ";
    cin >> num2;
    cout << "The Sum is : " << (num1 + num2) << endl;
    cout << "The def is : " << (num1 - num2) << endl;
    cout << "The Mul is : " << (num1 * num2) << endl;
    cout << "The Div is : " << (num1 / num2) << endl;
    cout << "The mod is : " << (num1 % num2) << endl;
    return 0 ;
}